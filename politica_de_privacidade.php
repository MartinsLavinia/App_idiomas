                    <div class="card-body p-4 p-md-5">
                        <p class="text-muted">Última atualização: 30 de Setembro de 2024</p>

                        <p>A sua privacidade é de extrema importância para o <strong>SpeakNut</strong>. Esta Política de Privacidade descreve como coletamos, usamos, armazenamos, compartilhamos e protegemos suas informações pessoais, em conformidade com a Lei Geral de Proteção de Dados (Lei nº 13.709/2018).</p>

                        <h3 class="mt-4">Definições Importantes</h3>
                        <ul>
                            <li><strong>Dados Pessoais:</strong> Qualquer informação relacionada a uma pessoa natural identificada ou identificável.</li>
                            <li><strong>Titular:</strong> Você, a pessoa natural a quem os dados pessoais se referem.</li>
                            <li><strong>Controlador:</strong> O SpeakNut, responsável por tomar as decisões sobre o tratamento dos seus dados.</li>
                            <li><strong>Tratamento:</strong> Toda operação realizada com dados pessoais, como coleta, uso, acesso, armazenamento, etc.</li>
                            <li><strong>LGPD:</strong> Lei Geral de Proteção de Dados Pessoais.</li>
                        </ul>

                        <h3 class="mt-4">1. Quais dados coletamos?</h3>
                        <p>Para fornecer nossos serviços, coletamos os seguintes tipos de informações:</p>
                        <ul>
                            <li><strong>Dados de Cadastro:</strong> Quando você cria uma conta, coletamos seu nome completo, endereço de e-mail e senha. Sua senha é armazenada de forma segura e criptografada, e não temos acesso a ela.</li>
                            <li><strong>Dados de Progresso e Uso:</strong> Coletamos informações sobre sua jornada de aprendizado, como o idioma que você escolheu, seu nível de proficiência (determinado pelo quiz inicial ou escolhido por você), as unidades e atividades que você completa, os resultados dos seus quizzes e o tempo gasto nas atividades.</li>
                            <li><strong>Dados de Navegação (Cookies):</strong> Utilizamos cookies estritamente necessários para o funcionamento da plataforma, como para manter sua sessão de login ativa.</li>
                            <li><strong>Dados de Administradores:</strong> Para administradores que se autenticam via Google, coletamos o e-mail e o nome associados à conta Google para garantir o acesso seguro à área administrativa.</li>
                        </ul>

                        <h3 class="mt-4">2. Para que usamos seus dados?</h3>
                        <p>O tratamento dos seus dados é realizado com base nas seguintes hipóteses legais da LGPD:</p>
                        <ul>
                            <li><strong>Execução de Contrato (Art. 7º, V):</strong> Usamos seus dados de cadastro e progresso para fornecer os serviços da plataforma que você contratou ao se cadastrar. Isso inclui criar sua conta, autenticar seu login, acompanhar seu progresso e personalizar sua experiência de aprendizado.</li>
                            <li><strong>Consentimento (Art. 7º, I):</strong> Ao criar sua conta e aceitar esta política, você nos fornece consentimento para coletar e utilizar seus dados para as finalidades aqui descritas.</li>
                            <li><strong>Legítimo Interesse (Art. 7º, IX):</strong> Podemos usar dados de forma agregada e anonimizada para analisar o uso da plataforma, visando melhorar nossos serviços, sem identificar individualmente os usuários.</li>
                            <li><strong>Segurança e Prevenção à Fraude:</strong> Utilizamos seus dados para proteger nossa plataforma e garantir a segurança das contas.</li>
                        </ul>

                        <h3 class="mt-4">3. Com quem compartilhamos seus dados?</h3>
                        <p>Nós não vendemos, alugamos ou compartilhamos suas informações pessoais com terceiros para fins de marketing.</p>
                        <ul>
                            <li><strong>Provedores de Serviço:</strong> Para a funcionalidade de login de administradores, utilizamos o serviço de autenticação do Google. Apenas os dados essenciais (e-mail, nome) são processados para essa finalidade.</li>
                            <li><strong>Obrigações Legais:</strong> Poderemos compartilhar suas informações se formos obrigados por lei ou por uma ordem judicial.</li>
                        </ul>                        

                        <h3 class="mt-4">4. Como protegemos seus dados?</h3>
                        <p>Levamos a segurança a sério e adotamos medidas para proteger seus dados:</p>
                        <ul>
                            <li><strong>Criptografia de Senhas:</strong> Utilizamos algoritmos modernos (<code>password_hash</code>) para criptografar sua senha, garantindo que ninguém, nem mesmo nossa equipe, tenha acesso a ela.</li>
                            <li><strong>Acesso Restrito:</strong> O acesso aos dados dos usuários é limitado apenas a administradores autorizados para fins de gerenciamento e suporte.</li>
                        </ul>

                        <h3 class="mt-4">5. Transferência Internacional de Dados</h3>
                        <p>Ao utilizar nossos serviços, seus dados podem ser armazenados e processados em servidores localizados fora do Brasil, como em provedores de nuvem (ex: AWS, Google Cloud). Garantimos que esses provedores possuem um nível de proteção de dados adequado e em conformidade com a LGPD.</p>
                        <h3 class="mt-4">6. Seus Direitos (LGPD)</h3>
                        <p>De acordo com a Lei Geral de Proteção de Dados (LGPD), você tem o direito de:</p>
                        <ul>
                            <li><strong>Acessar</strong> seus dados a qualquer momento.</li>
                            <li><strong>Corrigir</strong> informações incompletas, inexatas ou desatualizadas.</li>
                            <li><strong>Solicitar a exclusão</strong> de seus dados de nossa plataforma.</li>
                            <li><strong>Revogar o consentimento</strong> para o uso de seus dados.</li>
                        </ul>
                        <p>Para exercer esses direitos, você poderá, futuramente, acessar a página de perfil do usuário ou entrar em contato conosco através do e-mail fornecido nesta política.</p>

                        <h3 class="mt-4">7. Por quanto tempo mantemos seus dados?</h3>
                        <p>Manteremos seus dados pessoais enquanto sua conta estiver ativa em nossa plataforma ou até que você solicite a exclusão.</p>

                        <h3 class="mt-4">8. Dados de Menores de Idade</h3>
                        <p>Nossos serviços não são direcionados a menores de 18 anos. Não coletamos intencionalmente dados de menores. Caso um pai ou responsável identifique que um menor forneceu dados sem seu consentimento, deve entrar em contato conosco para que possamos remover tais informações.</p>

                        <h3 class="mt-4">9. Alterações nesta Política</h3>
                        <p>Podemos atualizar esta Política de Privacidade periodicamente. Notificaremos você sobre quaisquer alterações publicando a nova política nesta página e atualizando a data da "Última atualização". Recomendamos que você revise esta política regularmente.</p>

                        <h3 class="mt-4">10. Contato</h3>
                        <p>Se você tiver qualquer dúvida sobre esta Política de Privacidade ou sobre como tratamos seus dados, entre em contato com nosso Encarregado pelo Tratamento de Dados Pessoais (DPO) através do e-mail: <strong>contato@speaknut.com</strong>.</p>

